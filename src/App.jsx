import React from 'react';
import { BrowserRouter as Router, Routes, Route, Link, useLocation } from 'react-router-dom';
import Home from './pages/Home';
import Calculator from './pages/Calculator';
import HowItWorks from './pages/HowItWorks';
import { Leaf } from 'lucide-react';

// Simple Navbar Component inside App.jsx for ease
function Navbar() {
  const location = useLocation();
  const isActive = (path) => location.pathname === path ? "text-white font-bold" : "text-slate-200";

  return (
    <nav className="flex justify-between items-center p-6 bg-gradient-to-br from-green-600 to-emerald-500 shadow-sm sticky top-0 z-50">
      <Link to="/" className="text-xl font-black text-slate-100"><Leaf size={32}/></Link>
      <div className="flex gap-8 text-sm uppercase tracking-widest font-medium">
        <Link to="/" className={isActive('/')}>Home</Link>
        <Link to="/calculator" className={isActive('/calculator')}>Calculator</Link>
        <Link to="/how-it-works" className={isActive('/how-it-works')}>Methodology</Link>
      </div>
    </nav>
  );
}

function App() {
  return (
    <Router>
      <div className="min-h-screen bg-slate-50">
        <Navbar />
        <Routes>
          <Route path="/" element={<Home />} />
          <Route path="/calculator" element={<Calculator />} />
          <Route path="/how-it-works" element={<HowItWorks />} />
        </Routes>
      </div>
    </Router>
  );
}

export default App;

